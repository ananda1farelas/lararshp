        <h1>Sejarah RSHP Universitas Airlangga</h1>
        
        <p>
            Rumah Sakit Hewan dibentuk di Fakultas Kedokteran Hewan Universitas Airlangga secara resmi berdiri pada tanggal <b>1 Januari 1972</b> berdasarkan SK Menteri Pendidikan dan Kebudayaan Republik Indonesia bertempat di Jalan Setail no 1 Surabaya. Saat itu masih berupa klinik hewan yang menjadi bagian dari Departemen Klinik Veteriner, dimana klinik hewan ini juga menjadi wahana belajar mahasiswa Fakultas Kedokteran Hewan baik program S1 Kedokteran Hewan maupun Program Profesi Dokter Hewan atau lebih dikenal sebagai program Ko-Asistensi.
        </p>

        <p>
            Meskipun dengan sebutan klinik hewan namun berbagai aktivitas layanan dilakukan antara lain poliklinik, bedah, rawat inap, pemeriksaan darah, pemeriksaan radiografi bahkan layanan farmasi atau semacam apotek yang dapat dimanfaatkan klien menebus resep yang diberikan dokter untuk rawat jalan.
        </p>

        <p>
            Pada <b>1 Januari 2000</b>, Rumah Sakit Hewan pindah ke Jalan Mulyorejo Kampus C Unair Surabaya. Dengan kepindahan tersebut juga terjadi perubahan tata kelola Rumah Sakit Hewan, yang semula dikelola atau menjadi bagian dari Departemen Klinik Veteriner, berubah menjadi pengelolaan secara terpisah dan mandiri.
        </p>

        <p>
            Setelah berjalan cukup lama, kemudian Rumah Sakit Hewan direncanakan dilakukan pengembangan-pengembangan, termasuk membuat gedung yang lebih representatif dengan peralatan-peralatan yang lebih baru dan memenuhi standar layanan.
        </p>

        <p>
            Hari Kamis <b>4 April 2018</b> dilaksanakan soft launching gedung baru Rumah Sakit Hewan Pendidikan (RSHP) Unair dengan serangkaian kegiatan operasionalisasi oleh Dekan Fakultas Kedokteran Hewan yang terlaksana pada hari Selasa, <b>26 Juni 2018</b>, dilanjutkan dengan peresmian Rumah Sakit Hewan Pendidikan Universitas Airlangga oleh Menteri Riset dan Perguruan Tinggi pada tanggal <b>11 Juli 2018</b>.
        </p>

        <h2>Fungsi RSHP Universitas Airlangga</h2>
        <ul>
            <li>Merupakan sarana pendidikan mahasiswa D3, S1, S2, S3 baik di lingkungan Unair maupun di luar Unair</li>
            <li>Merupakan sarana pengabdian masyarakat</li>
            <li>Merupakan sarana tempat penelitian</li>
        </ul>

        <h2>Tujuan RSHP Universitas Airlangga</h2>
        <ul>
            <li>Menjadi rumah sakit hewan yang adaptif, kreatif, dan proaktif terhadap perkembangan pendidikan kesehatan hewan</li>
            <li>Menjadi Rumah Sakit Hewan yang mandiri dan bertata kelola baik (good governance)</li>
        </ul>

        <p>
            Untuk akuntabilitas RSHP Universitas Airlangga, RKAT sendiri setiap tahun oleh pihak manajemen RSHP Universitas Airlangga berkoordinasi dengan FKH Unair dan Direktorat Anggaran Unair dimulai penyusunan, telaah, dan penyerapan. Secara rutin RSHP Universitas Airlangga mengikuti audit yang dilakukan.
        </p>
